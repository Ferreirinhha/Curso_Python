#Condicionais

n1 = int(input('Digite um número de 1 a 10: '))

if n1 > 10 or n1 < 1: #Se condição for verdadeira, execulta o código.
    print('O número escolhido tem que ser maior que 1 e menor que 10.')
if n1 > 5:
    print('Número maior que 5.')
else: # Se não, vai execultar esse código
    print('Número menor que 5.')         